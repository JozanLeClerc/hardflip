package main

type ItemsNode struct {
	ID   int
	Dirs *DirsNode
	Host *HostNode
	next *ItemsNode
}

type ItemsList struct {
	head *ItemsNode
	last *ItemsNode
}

func (litems *ItemsList) add_back(node *ItemsNode) {
	new_node := node

	if litems.head == nil {
		litems.head = new_node
		litems.last = litems.head
		return
	}
	curr := litems.last
	// for curr.next != nil {
	// 	curr = curr.next
	// }
	new_node.ID = curr.ID + 1
	curr.next = new_node
	litems.last = curr.next
}

